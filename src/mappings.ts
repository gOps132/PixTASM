export function getUnicodeCharFromCP437(cp437Code : number) {
    const unicodeCodePoint = cp437ToUnicodeMap[cp437Code];
    if (unicodeCodePoint !== undefined) {
        return String.fromCharCode(unicodeCodePoint);
    }
    return ''; // Or throw an error for unmapped codes
}